# Chapter 6 Practice Questions

# 1. What are escape characters?

# Escape character are exempted from a string's interpretation. They are used to represent characters that are difficult or impossible to type directly. For example, the newline character is '\n'.

# 2. What do the \n and \t escape characters represent?

# n\ is newline and \t is tab.

# 3. How can you put a \ backslash character in a string?

# By using a double backslash \\.

# 4. The string value "Howl's Moving Castle" is a valid string. Why isn't it a problem that the single quote character in the word Howl's isn't escaped?

# Because the string is enclosed in double quotes.

# 5. If you don't want to put \n in your string, how can you write a string with newlines in it?

# Using ''' on both sides of the string or using the print() function again.

# 6. What do the following expressions evaluate to?

