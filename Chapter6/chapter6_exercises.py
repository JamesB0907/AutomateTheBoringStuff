# 'Hello, world!'[0:5] = 'Hello'
# 'Hello, world!'[:5] = 'Hello'
# 'Hello, world!'[3:] = 'lo, world!'

# 7. What do the following expressions evaluate to?

# 'Hello'.upper() = 'HELLO'
# 'Hello'.upper().isupper() = True
# 'Hello'.upper().lower() = 'hello'

