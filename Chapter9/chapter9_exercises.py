# Automate the Boring Stuff Chapter 9 Exercises

# 1. What is a relative path relative to?

# A relative path is relative to the current working directory.

# 2. What does an absolute path start with?

# An absolute path starts with the root folder.

# 3. What does Path('C:/Users') / 'Al' evaluate to on Windows?

# Path('C:/Users') / 'Al' evaluates to Path('C:/Users/Al') on Windows.

# 4. What does 'C:/Users' / 'Al' evaluate to on Windows?

# 'C:/Users' / 'Al' will raise a TypeError.

# 5. What do the os.getcwd() and os.chdir() functions do?

# os.getcwd() returns the current working directory, and os.chdir() changes the current working directory.

# 6. What are the . and .. folders?

# The . folder is the current folder, and the .. folder is the parent folder.

# 7. In C:\bacon\eggs\spam.txt, which part is the dir name, and which part is the base name?

# C:\bacon\eggs is the dir name, and spam.txt is the base name.

# 8. What are the three “mode” arguments that can be passed to the open() function?

# The three mode arguments are 'r' for read mode, 'w' for write mode, and 'a' for append mode.

# 9. What happens if an existing file is opened in write mode?

# If an existing file is opened in write mode, the file is erased and completely overwritten.

# 10. What is the difference between the read() and readlines() methods?

# The read() method reads the entire file as a single string, and the readlines() method reads the file as a list of strings, one string per line.

# 11. What data structure does a shelf value resemble?

# A shelf value resembles a dictionary value.

