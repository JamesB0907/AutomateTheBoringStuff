# 1. Why are functions advantageous to have in your programs?

# Functions are advantageous to have in your programs because they allow you to reuse code, make your code more readable, and make your code more modular.

# 2. When does the code in a function execute: when the function is defined or when the function is called?

# The code in a function executes when the function is called.

# 3. What statement creates a function?

# The def statement creates a function.

# 4. What is the difference between a function and a function call?

# A function is a block of code that performs a specific task and a function call is the code that runs the function.

# 5. How many global scopes are there in a Python program? How many local scopes?

# There is one global scope in a Python program and there can be multiple local scopes.

# 6. What happens to variables in a local scope when the function call returns?

# When the function call returns, the local scope is destroyed and the variables are forgotten.

# 7. What is a return value? Can a return value be part of an expression?

# A return value is the value that a function call evaluates to. A return value can be part of an expression.

# 8. If a function does not have a return statement, what is the return value of a call to that function?

# If a function does not have a return statement, the return value of a call to that function is None.

# 9. How can you force a variable in a function to refer to the global variable?

# You can force a variable in a function to refer to the global variable by using the global statement.

# 10. What is the data type of None?

# The data type of None is NoneType.

# 11. What does the import areallyourpetsnamederic statement do?

# The import areallyourpetsnamederic statement imports a module named areallyourpetsnamederic.

# 12. If you had a function named bacon() in a module named spam, how would you call it after importing spam?

# spam.bacon()

# 13. How can you prevent a program from crashing when it gets an error?

# You can prevent a program from crashing when it gets an error by using try and except statements.

# 14. What goes in the try clause? What goes in the except clause?

# The code that you want to run goes in the try clause and the code that you want to run if an error occurs goes in the except clause.